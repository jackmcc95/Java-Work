public class PrintfExamples {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// Using Printf
		
		int anInt = 9;
		double aDouble = 3.14;
	    String aString = "My String Value";
	    
	    System.out.println("Using printf");
	    System.out.printf("A Int value : %d \n",anInt);
	    System.out.printf("A double value : %f \n", aDouble);
	    System.out.printf("A string value : %s \n", aString);
	    System.out.printf("All values int %d and the %f then string %s", anInt, aDouble, aString);
	    

	}

}

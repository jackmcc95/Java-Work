package OOPFirst;

public class SimpleCalc {
	
	/**
	 * Add two numbers together and output the results to screen
	 *
	 *@param num1
	 *@param num2
	 *
	 */
	
	public void add(int num1, int num2) {
			System.out.println(num1+num2);
}
	
	
	
	/**
	 * Subtract Two number and output results to screen
	 * 
	 * @param num1
	 * @param num2
	 * 
	 */
	public void subtract (int num1, int num2) {
		System.out.println(num1-num2);
		
	}
	
	/**
	 * Multiply Two Numbers and output to screen
	 * 
	 * @param num1
	 * @param num2
	 * 
	 */
	
	public void multiply (int num1, int num2) {
		System.out.println(num1 * num2);
	}
	
	
	public void divide (int num1, int num2) {
		System.out.println(num1 / num2);
	}
}